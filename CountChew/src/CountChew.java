import static java.lang.System.out;

public class CountChew {

	public static void main(String[] args) {
		
		for (int count = 0; count < 10; count++) {
			out.print("I've Chewed ");
			out.print(count);
			out.println(" time(s)! ");
		}
		out.println("10 times! Sweet!");
		out.println("I can swallow now");
	}

}
